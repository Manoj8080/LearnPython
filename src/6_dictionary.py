#dictionay creation
#dict={key1:value1, key2:value2,...}
my_dict={'name':'manoj','age':21,'city':'amalapuram'}
print(my_dict)

#dict operations
#access dictionary key values
print(my_dict['name'])

#add new key value pairs
my_dict['job']='student'
print(my_dict)

#remove a key value pair
del my_dict['age']
print(my_dict)

#replace value of a key
my_dict['city']=('amp')
print(my_dict)