# string concatenation(+)
a="madey"
b="manoj "
print(a+" "+b)

# string repetation(*)
print(b*3)

# string comparison and relational operators (<, >, ==, !=, <=, >=)
fruit1="apple"
fruit2="kiwi"
print(fruit1>fruit2)
print(fruit1<fruit2)
print(fruit1==fruit2)
print(fruit1!=fruit2)
print(fruit1<=fruit2)
print(fruit1>=fruit2)

# string membership operators (in , not in)
sentence="the quick brown fox jumps over the lazy dog"
print("dog" in sentence)
print("lazy" not in sentence)

# string slicing or indexing
text="hello world"
print(text[0:5])